def template(
    template_id: str,
    *,
    sector_id: str,
    title: str,
    description: str = "",
    task_type: str = "STANDARD",
    start: str,
    due: str,
    priority: str = "NORMAL",
    critical: bool = False,
    recurrence_type: str = "WEEKDAYS",
    weekdays: list[int] | None = None,
    month_days: list[int] | None = None,
    weeks_of_month: list[int] | None = None,
    goal_target: int | None = None,
    goal_unit: str = "",
    goal_group_key: str = "",
    requires_comment: bool = False,
    requires_evidence: bool = False,
    requires_review: bool = False,
    allow_not_applicable: bool = True,
    conditional: bool = False,
    instructions: str = "",
    subtasks: list[dict[str, object]] | None = None,
) -> dict[str, object]:
    return {
        "id": template_id,
        "organization_id": ORGANIZATION_ID,
        "unit_id": UNIT_ID,
        "sector_id": sector_id,
        "title": title,
        "description": description,
        "task_type": task_type,
        "start_time": time.fromisoformat(start),
        "due_time": time.fromisoformat(due),
        "priority": priority,
        "is_critical": critical,
        "goal_target": goal_target,
        "goal_unit": goal_unit,
        "goal_group_key": goal_group_key,
        "requires_comment_on_completion": requires_comment,
        "requires_evidence": requires_evidence,
        "requires_manager_review": requires_review,
        "allow_not_applicable": allow_not_applicable,
        "is_conditional": conditional,
        "instructions": instructions,
        "active": True,
        "recurrence_rule": {
            "recurrence_type": recurrence_type,
            "interval_value": 1,
            "weekdays": weekdays or [],
            "month_days": month_days or [],
            "weeks_of_month": weeks_of_month or [],
            "custom_rule_json": {},
            "start_date": date(2026, 7, 1),
            "end_date": None,
        },
        "subtasks": subtasks or [],
    }


