def seed_core() -> None:
    upsert_organization(
        {
            "id": ORGANIZATION_ID,
            "name": "Vitalle Odontologia & Harmonização",
            "slug": "vitalle-odontologia",
            "timezone": "America/Sao_Paulo",
            "is_active": True,
        }
    )
    upsert_unit(
        {
            "id": UNIT_ID,
            "organization_id": ORGANIZATION_ID,
            "name": "Vitalle Principal",
            "slug": "vitalle-main",
            "timezone": "America/Sao_Paulo",
            "is_active": True,
            "sort_order": 1,
        }
    )
    upsert_system_setting(
        {
            "organization_id": ORGANIZATION_ID,
            "unit_id": UNIT_ID,
            "key": "timezone",
            "value_json": {"value": "America/Sao_Paulo"},
            "updated_by": None,
        }
    )
    for sector in SECTORS:
        upsert_sector(
            {
                **sector,
                "organization_id": ORGANIZATION_ID,
                "unit_id": UNIT_ID,
                "status": "active",
            }
        )
    for payload in TEMPLATES:
        upsert_task_template(payload)


def seed_demo_users() -> None:
    for user in USERS:
        upsert_user(
            {
                "id": user["id"],
                "organization_id": ORGANIZATION_ID,
                "unit_id": UNIT_ID,
                "email": user["email"],
                "full_name": user["full_name"],
                "role": user["role"],
                "is_active": True,
                "is_demo": True,
            }
        )
        upsert_user_profile(
            {
                "user_id": user["id"],
                "display_name": user["display_name"],
                "title": user["title"],
                "bio": "",
            }
        )
        for index, sector_id in enumerate(user["sector_ids"]):
            upsert_user_sector_assignment(
                {
                    "user_id": user["id"],
                    "sector_id": sector_id,
                    "is_primary": index == 0,
                    "is_active": True,
                }
            )


def main() -> None:
    parser = argparse.ArgumentParser(description="Seed Vitalle OPS demo data")
    parser.add_argument("--demo-users", action="store_true", help="Seed demo auth/profile personas too")
    parser.add_argument("--sync-today", action="store_true", help="Generate today's daily operation after seeding")
    args = parser.parse_args()
    load_local_env()

    seed_core()
    if args.demo_users:
        seed_demo_users()
    if args.sync_today:
        sync_daily_operation(ORGANIZATION_ID, UNIT_ID, date.today(), "America/Sao_Paulo", "seed", actor_user_id="demo-admin")

    print(
        {
            "organization_id": ORGANIZATION_ID,
            "unit_id": UNIT_ID,
            "sectors": len(SECTORS),
            "templates": len(TEMPLATES),
            "demo_users": bool(args.demo_users),
            "synced_today": bool(args.sync_today),
        }
    )


if __name__ == "__main__":
    main()
